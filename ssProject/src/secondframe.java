    boolean validn()
    // validations for entered data
    {
        if (jTextField1.getText().length()<3)
        {
            JOptionPane.showMessageDialog(rootPane, "Enter proper name");
            return false;
        }
        else if (jTextField2.getText().length()!=10)
        {
            JOptionPane.showMessageDialog(rootPane, "Enter valid phone number");
            return false;
        }
        else if (jTextArea1.getText().length()<5)
        {
            JOptionPane.showMessageDialog(rootPane, "Enter proper address");
            return false;
        }
        else if (jRadioButton1.isSelected()==false && jRadioButton2.isSelected()==false)
        {
            JOptionPane.showMessageDialog(rootPane, "select your gender");
            return false;
        }
        else if (jComboBox1.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(rootPane, "Choose a Usertype");
            return false;
        }
        else if (jTextField3.getText().length()==0)
        {
            JOptionPane.showMessageDialog(rootPane, "Enter a username");
            return false;
        }
        else if (jPasswordField1.getText().length()<5)
        {
            JOptionPane.showMessageDialog(rootPane, "Enter Strong password of atleast 6 characters");
            return false;
        }
        
        else
        {
            return true;
        }
    }

