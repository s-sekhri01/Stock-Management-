    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
       //Search with name and add all records of that name in usertable 
        
        try
        {
            Connection mycon=DriverManager.getConnection(myurl,dbuser,dbpass);
            try
            {
             DefaultTableModel mymodel =(DefaultTableModel)jTable1.getModel();
             String q="select * from usertable where name like ?";
             PreparedStatement myst=mycon.prepareStatement(q);
             myst.setString(1, "%"+jTextField1.getText()+"%");
            
             ResultSet myres=myst.executeQuery();
             
             if(myres.next()==true)
             {
                mymodel.setRowCount(0);
               do
               {
                 
                 String n,ph,gen,un;
                 n=myres.getString("name");
                 ph=myres.getString("phone");
                 gen=myres.getString("gender");
                 un=myres.getString("username"); 

                Object myrow[] = {n,ph,gen,un};

                mymodel.addRow(myrow);
               }
                while(myres.next());
            
            }
           else
              {
                JOptionPane.showMessageDialog(rootPane, "No Records found");
                 clearfields();
                 jButton3.setEnabled(false);
                 jButton4.setEnabled(false);
                 mymodel.setRowCount(0);
                }

           }
          catch(Exception e)
            {
             JOptionPane.showMessageDialog(rootPane, "Error " + e.getMessage());
              }
           finally
             {
              mycon.close();
              }
         }
        catch(Exception e)
          {
            JOptionPane.showMessageDialog(rootPane, "Error in connection " + e.getMessage());
           }
    }//GEN-LAST:event_jButton5ActionPerformed

