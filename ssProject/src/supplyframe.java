    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // to show the selected category in fields
        String supidref= jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
        
        try
        {
            Connection mycon=DriverManager.getConnection(myurl,dbuser,dbpass);
            try
            {
             String q="select * from suppliertable where supid=?";
             PreparedStatement myst=mycon.prepareStatement(q);
             myst.setString(1,supidref);
            
             ResultSet myres=myst.executeQuery();
             
             if(myres.next()==true)
             {
                 jLabel6.setText(myres.getString("SupId"));
               jTextField1.setText(myres.getString("SupName"));
               jTextField2.setText(myres.getString("EmailId"));
               jTextField3.setText(myres.getString("PhoneNo"));
               
                jButton2.setEnabled(true);
                jButton3.setEnabled(true);
 
            }
           else
              {
                JOptionPane.showMessageDialog(rootPane, "Cannot Fetch Data");
                 clearfields();
                 jButton2.setEnabled(false);
                 jButton3.setEnabled(false);
                }

           }
          catch(Exception e)
            {
             JOptionPane.showMessageDialog(rootPane, "Error " + e.getMessage());
              }
           finally
             {
              mycon.close();
              }
         }
        catch(Exception e)
          {
            JOptionPane.showMessageDialog(rootPane, "Error in connection " + e.getMessage());
           }
    }//GEN-LAST:event_jTable1MouseClicked

