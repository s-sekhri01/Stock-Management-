    void getsupid()
    {
        try
        {
            Connection mycon=DriverManager.getConnection(myurl,dbuser,dbpass);
            try
            {
                String q="select * from suppliertable order by supid desc";
                PreparedStatement myst=mycon.prepareStatement(q);
                ResultSet myres = myst.executeQuery();

                if(myres.next())
                  { 
                    int supid = myres.getInt("supid");
                    supid++;
                    jLabel6.setText(String.valueOf(supid));
                   }
                 else
                  {
                    jLabel6.setText("1");
                    }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(rootPane,"Error "+e.getMessage());
            }
            finally
            {
                mycon.close();
            }

        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane,"Error is "+e.getMessage());
        }
    }
       void getdata()
       {
            try
        {
            Connection mycon=DriverManager.getConnection(myurl,dbuser,dbpass);
            try
            {
             DefaultTableModel mymodel =(DefaultTableModel)jTable1.getModel();
             String q="select * from suppliertable";
             PreparedStatement myst=mycon.prepareStatement(q);
             ResultSet myres=myst.executeQuery();
             
             if(myres.next()==true)
             {
                 mymodel.setRowCount(0);
               do
               {
                 
                 String supid,supname,emailid,phoneno;
                 supid=myres.getString("SupId");
                 supname=myres.getString("SupName");
                 emailid=myres.getString("EmailId");
                 phoneno=myres.getString("PhoneNo");
                 
                Object myrow[] = {supid,supname,emailid,phoneno};

                mymodel.addRow(myrow);
               }
                while(myres.next());
            
            }
           else
              {
                JOptionPane.showMessageDialog(rootPane, "No Records found");
                 clearfields();
                 
                 mymodel.setRowCount(0);
                }

           }
          catch(Exception e)
            {
             JOptionPane.showMessageDialog(rootPane, "Error " + e.getMessage());
              }
           finally
             {
              mycon.close();
              }
         }
        catch(Exception e)
          {
            JOptionPane.showMessageDialog(rootPane, "Error in connection " + e.getMessage());
           }
       }

